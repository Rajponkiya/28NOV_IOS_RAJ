// WAP to make array of dictionaries and get a value from specific dictionaries.

import Foundation

var names: [String: String] = [
    "india": "Delhi",
    "srilanka": "Colombo",
    "england": "London"
]

for (key, value) in names {
    print("Key: \(key) -------- Value: \(value)")
}


